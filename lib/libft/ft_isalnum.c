#include "libft.h"

int	ft_isalnum(int c)
{
	int	alnum;

	alnum = 1;
	if ((c < 48 || c > 57) && (c < 'A' || c > 'Z')
			&& (c < 'a' || c > 'z'))
		alnum = 0;
	return (alnum);
}
